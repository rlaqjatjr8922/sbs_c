#include <stdio.h>

int main() {

    //배열
    //리스트
    //  자료형 변수명[개수];
    // 자료형 변수명[] = {1,2,3};

    int num[3] = {1,2,3};
    char ch[3] = {'a','b','c'};


printf("num[%d] = %d \n",0,num[0]);
printf("num[%d] =%d\n",1,num[1]);
printf("num[%d] = %d\n",2,num[2]);

printf("ch[%d] = %c\n",0,num[0]);
printf("ch[%d] = %c\n",1,ch[1]);
printf("ch[%d] = %c\n",2,ch[2]);

return 0;

}